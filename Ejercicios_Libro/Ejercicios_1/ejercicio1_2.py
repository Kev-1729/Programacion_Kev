# Declarar las variables a y b
a = 5
b = 3

# Realizar operaciones aritméticas
suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b
modulo = a % b

# Imprimir los resultados
print("Suma:", suma)  # Salida: Suma: 8
print("Resta:", resta)  # Salida: Resta: 2
print("Multiplicación:", multiplicacion)  # Salida: Multiplicación: 15
print("División:", division)  # Salida: División: 1.6666666666666667
print("Módulo:", modulo)  # Salida: Módulo: 2