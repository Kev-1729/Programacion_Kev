# Declarar la variable número con el valor 10
numero = 10

# Convertir a float, cadena y booleano
numero_float = float(numero)
numero_cadena = str(numero)
numero_booleano = bool(numero)

# Imprimir el tipo de cada conversión
print(type(numero_float))  # Salida: <class 'float'>
print(type(numero_cadena))  # Salida: <class 'str'>
print(type(numero_booleano))  # Salida: <class 'bool'>